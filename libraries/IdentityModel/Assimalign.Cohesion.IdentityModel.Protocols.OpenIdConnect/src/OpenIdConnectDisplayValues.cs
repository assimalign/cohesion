namespace Assimalign.Cohesion.IdentityModel.Protocols.OpenIdConnect;

/// <summary>
/// Defines the <c>display</c> parameter values (OpenID Connect Core 1.0 §3.1.2.1).
/// </summary>
public static class OpenIdConnectDisplayValues
{
    /// <summary>
    /// A full user-agent page view (<c>page</c>).
    /// </summary>
    public const string Page = "page";

    /// <summary>
    /// A popup user-agent window (<c>popup</c>).
    /// </summary>
    public const string Popup = "popup";

    /// <summary>
    /// A device-native touch interface (<c>touch</c>).
    /// </summary>
    public const string Touch = "touch";

    /// <summary>
    /// A feature-phone interface (<c>wap</c>).
    /// </summary>
    public const string Wap = "wap";
}
